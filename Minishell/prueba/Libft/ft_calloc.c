#include "libft.h"

void	*ft_calloc(size_t num, size_t size)
{
	void	*ptr;

	ptr = malloc(num * size);
	if (ptr == NULL)
	{
		return (0);
	}
	ft_memset(ptr, 0, num * size);
	return (ptr);
}
