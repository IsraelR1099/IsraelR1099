#include <unistd.h>
#include <stdio.h>

void	ft_print_alphabet(void)
{
	char	i;
	char	a;

	i = 'a';
	a = ' ';
	while (i <= 'z')
	{
		write(1, &i, 1);
		write(1, &a, 1);
	i++;
	}
	write(1, "\n", 1);
}	
